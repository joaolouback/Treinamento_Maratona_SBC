#include <iostream> // Inclui a biblioteca de entrada e saída padrão (input/output stream), 
                    // que permite usar comandos como 'cout' e 'cin'.

using namespace std; // Diz ao compilador para usar o "namespace" padrão da linguagem C++, 
                     // chamado 'std'. Isso permite usar 'cout' ao invés de 'std::cout'.

// Função principal que é o ponto de entrada do programa
int main(){
    // 'cout' é usado para imprimir na tela. Aqui ele imprime "Hello World!".
    // 'endl' é usado para quebrar a linha (é como '\n', mas garante que a saída seja limpa).
    cout << "Hello World!" << endl;

    // 'return 0;' indica que o programa terminou com sucesso.
    return 0;
}
